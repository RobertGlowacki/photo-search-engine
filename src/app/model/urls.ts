/**
 * Model class for urls links.
 */
export interface Urls {

  /**
   * Link for raw data.
   */
  raw: string;

  /**
   * Link.
   */
  link: string;

  /**
   * Link for regular data.
   */
  regular: string;

  /**
   * Link for small image.
   */
  small: string;

  /**
   * Link for thumb.
   */
  thumb: string;

}
