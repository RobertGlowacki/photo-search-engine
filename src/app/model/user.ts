/**
 * Model class for user.
 */
export interface User {

  /**
   * User name presented as "firstName lastName".
   */
  name: string;

  /**
   * Nickname.
   */
  username: string;

  /**
   * User's first name.
   */
  firstName: string;

  /**
   * User's last name.
   */
  lastName: string;

}
